ergm.control<-function(prop.weights="default",prop.args=NULL,
                       prop.weights.diss="default",prop.args.diss=NULL,
                       nr.maxit=100, calc.mcmc.se=TRUE, hessian=FALSE,
                       compress=FALSE,
                       maxNumDyadTypes=10000, 
                       maxedges=20000,
                       maxchanges=1000000,
                       MPLEsamplesize=50000, 
                       MPLEtype=c("glm", "penalized"),
                       trace=0,
                       steplength=0.5,
                       drop=TRUE,
                       force.mcmc=FALSE,
                       mcmc.precision=0.05,
                       metric="Likelihood",
                       method=c("BFGS","Nelder-Mead"),
                       trustregion=20,
                       style=c("Newton-Raphson","Robbins-Monro","Stochastic-Approximation"),
                       phase1_n=NULL, initial_gain=NULL, 
                       nsubphases="maxit", niterations=NULL, phase3_n=NULL,
                       dyninterval=1000,
                       parallel=0,
                       returnMCMCstats=TRUE){
control<-list(prop.weights=prop.weights,
             prop.args=NULL,
             prop.weights.diss=prop.weights.diss,
             prop.args.diss=prop.args.diss,
             nr.maxit=nr.maxit,
             calc.mcmc.se=calc.mcmc.se,
             hessian=hessian,
             compress=compress,
             maxNumDyadTypes=maxNumDyadTypes,
             maxedges=maxedges,
             maxchanges=maxchanges,
             MPLEsamplesize=MPLEsamplesize,
             MPLEtype=match.arg(MPLEtype),
             trace=trace,
             steplength=steplength,
             drop=drop,
             force.mcmc=force.mcmc,
             mcmc.precision=mcmc.precision,
             metric=match.arg(metric),
             method=match.arg(method),
             trustregion=trustregion,
             style=match.arg(style),
             phase1_n=phase1_n,
             initial_gain=initial_gain,
             nsubphases=match.arg(nsubphases),
             niterations=niterations,
             phase3_n=phase3_n,
             dyninterval=dyninterval,
             parallel=parallel,
             returnMCMCstats=returnMCMCstats)
  control
}
